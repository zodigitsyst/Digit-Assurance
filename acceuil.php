<strong>Qu'est-ce qu'une assurance ?</strong>
<br/>
Une assurance est, par définition, un système qui permet de prémunir un individu, une association ou une entreprise contre les conséquences financières et économiques liées à la survenance d'un risque (événement aléatoire) particulier.
</div>
<div class="rol"><strong> Role de l'assurance: </strong><br/>
L'assureur propose à l'assuré un contrat de garantie qui va le couvrir contre les risques financiers qui pourraient survenir suite à un événement imprévu
</div>
<div class="import"><strong>Importance d'une assurance:</strong><br/>
La vocation traditionnelle de l’assurance est de permettre le remplacement de biens détruits ou subtilisés.Les assurances de biens et les assurances de responsabilité ont pour but de protéger le patrimoine de l’assuré.
